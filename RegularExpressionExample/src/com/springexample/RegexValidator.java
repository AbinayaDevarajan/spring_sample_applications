package com.springexample;

public class RegexValidator {
	private boolean entityNoValid;
	private boolean entityNameValid;
	
	
	public boolean isEntityNoValid() {
		return entityNoValid;
	}
	public void setEntityNoValid(boolean entityNoValid) {
		this.entityNoValid = entityNoValid;
	}
	public boolean isEntityNameValid() {
		return entityNameValid;
	}
	public void setEntityNameValid(boolean entityNameValid) {
		this.entityNameValid = entityNameValid;
	}
	

}
